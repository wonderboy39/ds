package com.coding.huffman;

public class Priority {
	private int priority;
	private char data;
	public Priority(){}
	public int getPriority() {
		return priority;
	}
	public void setPriority(int priority) {
		this.priority = priority;
	}
	public char getData() {
		return data;
	}
	public void setData(char data) {
		this.data = data;
	}
	
}
