package com.coding.huffman;

public class Priority {
	private float priority;
	private char data;

	public Priority(){}
	
	public float getPriority() {
		return priority;
	}
	public void setPriority(float priority){
		this.priority = priority;
	}
	public char getData() {
		return data;
	}
	public void setData(char data) {
		this.data = data;
	}
	
}
